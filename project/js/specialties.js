// Мини-игра для торгового дела
function initTradeGame() {
    const checkScenarioBtn = document.getElementById('checkScenarioBtn');
    const hintBtn = document.getElementById('hintBtn');
    const nextScenarioBtn = document.getElementById('nextScenarioBtn');
    const message = document.getElementById('gameMessage');
    const currentLevel = document.getElementById('currentLevel');
    const scoreElement = document.getElementById('score');
    const options = document.querySelectorAll('.option');
    
    if (checkScenarioBtn) {
        let currentScenario = 1;
        let score = 0;
        let scenarios = [
            {
                question: "Клиент в магазине электроники хочет купить смартфон, но сомневается между двумя моделями. Какой подход будет наиболее эффективным?",
                options: [
                    "Настоятельно рекомендовать более дорогую модель",
                    "Выяснить потребности клиента и подобрать оптимальный вариант",
                    "Предложить акционный товар без объяснения преимуществ",
                    "Оставить клиента самостоятельно выбирать"
                ],
                correct: 1,
                hint: "Эффективные продажи строятся на понимании потребностей клиента, а не на навязывании товара."
            },
            {
                question: "В магазин пришел недовольный клиент с бракованным товаром. Ваши действия?",
                options: [
                    "Сразу предложить возврат денег",
                    "Выслушать претензию, извиниться и предложить решение",
                    "Направить к менеджеру без объяснений",
                    "Указать на условия гарантии и отказать в возврате"
                ],
                correct: 1,
                hint: "Клиентоориентированный подход предполагает активное слушание и поиск взаимовыгодного решения."
            },
            {
                question: "Как эффективно представить новый товар постоянным клиентам?",
                options: [
                    "Разослать массовую рассылку с описанием товара",
                    "Персонально предложить товар с учетом истории покупок клиента",
                    "Разместить товар на видном месте в магазине",
                    "Дождаться, когда клиенты сами спросят о новинке"
                ],
                correct: 1,
                hint: "Персонализированные предложения значительно повышают конверсию и лояльность клиентов."
            }
        ];

        function loadScenario(scenarioIndex) {
            const scenario = scenarios[scenarioIndex - 1];
            document.getElementById('scenarioText').textContent = scenario.question;
            
            options.forEach((option, index) => {
                option.querySelector('span').textContent = scenario.options[index];
                option.dataset.correct = index === scenario.correct ? 'true' : 'false';
                option.classList.remove('selected', 'correct', 'incorrect');
            });
            
            currentLevel.textContent = scenarioIndex;
            message.textContent = '';
            nextScenarioBtn.style.display = 'none';
        }

        function checkAnswer() {
            const selectedOption = document.querySelector('.option.selected');
            if (!selectedOption) {
                message.innerHTML = '<i class="fas fa-exclamation-circle"></i> Пожалуйста, выберите вариант ответа';
                message.style.color = '#ef4444';
                message.style.backgroundColor = 'rgba(239, 68, 68, 0.1)';
                message.style.borderColor = '#ef4444';
                return;
            }

            const isCorrect = selectedOption.dataset.correct === 'true';
            
            options.forEach(option => {
                if (option.dataset.correct === 'true') {
                    option.classList.add('correct');
                } else if (option === selectedOption && !isCorrect) {
                    option.classList.add('incorrect');
                }
            });

            if (isCorrect) {
                score += 10;
                scoreElement.textContent = score;
                message.innerHTML = '<i class="fas fa-check-circle"></i> Правильно! Отличное решение!';
                message.style.color = '#10b981';
                message.style.backgroundColor = 'rgba(16, 185, 129, 0.1)';
                message.style.borderColor = '#10b981';
                
                if (currentScenario < scenarios.length) {
                    nextScenarioBtn.innerHTML = '<i class="fas fa-arrow-right"></i> Следующая ситуация';
                } else {
                    nextScenarioBtn.innerHTML = '<i class="fas fa-redo"></i> Начать заново';
                }
                nextScenarioBtn.style.display = 'flex';
            } else {
                message.innerHTML = '<i class="fas fa-exclamation-circle"></i> Неверное решение. Попробуйте еще раз!';
                message.style.color = '#ef4444';
                message.style.backgroundColor = 'rgba(239, 68, 68, 0.1)';
                message.style.borderColor = '#ef4444';
            }
        }

        function showHint() {
            const scenario = scenarios[currentScenario - 1];
            alert(scenario.hint);
        }

        function nextScenario() {
            if (currentScenario < scenarios.length) {
                currentScenario++;
                loadScenario(currentScenario);
            } else {
                // Завершение игры
                currentScenario = 1;
                score = 0;
                scoreElement.textContent = score;
                loadScenario(currentScenario);
            }
        }

        // Обработчики событий
        options.forEach(option => {
            option.addEventListener('click', function() {
                options.forEach(opt => opt.classList.remove('selected'));
                this.classList.add('selected');
            });
        });

        checkScenarioBtn.addEventListener('click', checkAnswer);
        hintBtn.addEventListener('click', showHint);
        nextScenarioBtn.addEventListener('click', nextScenario);

        // Инициализация игры
        loadScenario(currentScenario);
    }
}

